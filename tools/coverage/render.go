// Render returns the full Markdown body of docs/user/coverage-matrix.md.
//
// The page is fully generated (no splice markers): this keeps it inside the
// `tools/check-docs-mdx` MDX gate with zero exclusions — there are no HTML
// comments, no bare `{`, and no autolinks anywhere in the output. When noTitle
// is set the leading H1 is omitted (parity with `tools/bom`); deterministic is
// accepted for parity but the output carries no per-run metadata regardless, so
// it is always byte-stable.
func Render(m Matrix, deterministic, noTitle bool) string {
	_ = deterministic // output is unconditionally deterministic; flag kept for CLI parity

	var b strings.Builder
	if !noTitle {
		b.WriteString("# Recipe & CLI Coverage Matrix\n\n")
	}
	b.WriteString(preamble(m))
	b.WriteString("\n")
	b.WriteString(renderTable("Critical user journeys (CUJs)", m, KindCUJ))
	b.WriteString("\n")
	b.WriteString(renderTable("CLI verbs", m, KindCLI))
	return b.String()
}

// preamble is the hand-authored prose, kept here (rather than in the doc file)
// because the page is fully regenerated. It states the distinction from
// #1224's declared_coverage Coverage column and the link-only TestGrid rule.
func preamble(m Matrix) string {
	var b strings.Builder
	b.WriteString("This page is generated by `make coverage-docs` (`tools/coverage`) and ")
	b.WriteString("refreshed weekly by the ")
	b.WriteString("[coverage-matrix-refresh](https://github.com/NVIDIA/aicr/actions/workflows/coverage-matrix-refresh.yaml) ")
	b.WriteString("workflow. Do not edit by hand.\n\n")

	b.WriteString("It is a **structural** matrix: it reports *which* CUJs and CLI verbs are ")
	b.WriteString("exercised by an in-repo test or demo, on what hardware class, and at what ")
	b.WriteString("cadence. It does **not** report live pass/fail — that posture is a link into ")
	b.WriteString("the AICR TestGrid (which carries `aicr_version` as a column facet), never ")
	b.WriteString("embedded here.\n\n")

	b.WriteString("This is a **different axis** from the per-recipe **Coverage** column in the ")
	b.WriteString("recipe-health matrix (ADR-009 `declared_coverage`): that column counts ")
	b.WriteString("declared named checks per validation phase for a single recipe, whereas this ")
	b.WriteString("matrix answers \"which journeys and CLI verbs are exercised, where, and how ")
	b.WriteString("often.\" The reverse cross-link from the recipe-health page is added by RQ1 ")
	b.WriteString("(#1283) once that page ships. See also the [CLI reference](./cli-reference.md).\n\n")

	b.WriteString("**AICR-version axis.** Scheduled UAT builds only the current checkout, so ")
	b.WriteString("the live axis today is `")
	b.WriteString(strings.Join(m.VersionAxis, "`, `"))
	b.WriteString("`. The multi-version matrix (main + the previous N stable releases) is ")
	b.WriteString("owned by the dynamic-clusters epic (DC5); per-version posture will be a ")
	b.WriteString("TestGrid link, not a cell here.\n\n")

	b.WriteString("**Legend.** `covered` = exercised by an executable *journey* signal — a ")
	b.WriteString("chainsaw/KWOK test or a wired nightly UAT runner; `not-yet-covered` = a ")
	b.WriteString("shipping capability no such journey touches yet; `stubbed` = UAT assets exist ")
	b.WriteString("but no scheduled workflow runs them. This tracks CUJ/CLI journey coverage — ")
	b.WriteString("per-package Go unit-test coverage is a separate gate (`make test-coverage`), ")
	b.WriteString("not reflected here.\n")
	return b.String()
}

// renderTable emits one Markdown table for the rows of the given kind. A
// section with no rows still renders its header so the page shape is stable.
func renderTable(heading string, m Matrix, kind Kind) string {
	var b strings.Builder
	b.WriteString("## " + heading + "\n\n")
	b.WriteString("| Item | Exercised by | Hardware | Cadence | Status | Notes |\n")
	b.WriteString("|------|--------------|----------|---------|--------|-------|\n")
	for _, r := range m.Rows {
		if r.Kind != kind {
			continue
		}
		note := r.Note
		if note == "" {
			note = "—"
		}
		fmt.Fprintf(&b, "| `%s` | %s | %s | %s | %s | %s |\n",
			r.Item, exercisedBy(r.Harnesses), r.Hardware, r.Cadence, r.Status, note)
	}
	return b.String()
}

// exercisedBy renders the harness set in canonical order, or an em dash when
// nothing exercises the item.
func exercisedBy(h map[Harness]bool) string {
	var parts []string
	for _, name := range allHarnesses {
		if h[name] {
			parts = append(parts, string(name))
		}
	}
	if len(parts) == 0 {
		return "—"
	}
	return strings.Join(parts, ", ")
}
